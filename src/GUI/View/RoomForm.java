    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        try{
            int row = table.getSelectedRow();
            if(row == -1){
                Room r = new Room();
                //p.setId(Integer.parseInt(idField.getText()));
                r.setRoomNo(Integer.parseInt(roomNoField.getText()) );
                r.setCapacity(Integer.parseInt(capacityField.getText()));
                r.setPersonID((Person)pcmb.getSelectedItem());
                vr.create(r);
            }else{
                Room r = vtm.getRoom(row);
                //Id nuk e lejojm me ndryshu vetem emertimin ose fielda tjere qe mundeni me pas
                r.setRoomNo(Integer.parseInt(roomNoField.getText()) );
                r.setCapacity(Integer.parseInt(capacityField.getText()));
                r.setPersonID((Person)pcmb.getSelectedItem());
                vr.edit(r);
            }
            //E krijojm ni metode per me i clear fieldat mbasi ti shtojme
            clear();
            loadTable();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if(row != -1){
            Object[] ob = {"Yes","No"};
            int i = JOptionPane.showOptionDialog(this,"Do you want to delete this?","Delete", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if(i == 0){
                Room r = vtm.getRoom(row);
                try{
                    vr.delete(r);
                }catch(CrudFormException ex){
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
                clear();
                loadTable();
            }else{
                clear();
            }
        }else{
            JOptionPane.showMessageDialog(this, "You haven't selected anything to delete!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

