    public void loadTable(){
        try{
            List<Room> lista = vr.findAll();
            vtm.addList(lista);
            table.setModel(vtm);
            vtm.fireTableDataChanged();
        }catch(CrudFormException ex){
            Logger.getLogger(PersonForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Room r = vtm.getRoom(selectedIndex);
                    idField.setText(r.getId().toString());
                    roomNoField.setText(r.getRoomNo().toString());
                    capacityField.setText(r.getCapacity().toString());
                    pcmb.setSelectedItem(r.getPersonID());
                    personCMB.repaint();
                }
            }
        });
    }
    
    public void loadComboBox(){
        try{
            List<Person> lista = ppr.findAll();
            pcmb.add(lista);
            personCMB.setModel(pcmb);
            personCMB.repaint();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
